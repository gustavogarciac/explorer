// Switch
let bloodType = "c"
switch (bloodType) {
    case "a":
        console.log('TIPO SANGUÍNEO: A')
        break;
    case "ab":
        console.log("TIPO SANGUÍNEO: AB")
        break;
    case "b":
        console.log("TIPO SANGUÍNEO: B")
        break;
    case "c":
        console.log("TIPO SANGUÍNEO: C")
        break;
    default:
        console.log("[ERRO] TIPO SANGUÍNEO NÃO ENCONTRADO.")
        break;
}